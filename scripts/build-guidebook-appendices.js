import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

const root = fileURLToPath(new URL("../", import.meta.url));
const read = (p) => JSON.parse(readFileSync(`${root}${p}`, "utf8"));
const HEADER = "<!-- Generated by scripts/build-guidebook-appendices.js â€” do not edit by hand. -->\n\n";
const esc = (s) => String(s).replace(/\|/g, "\\|");
// Extra hardening for values interpolated inside `code spans`: a literal
// backtick in the source value would otherwise terminate the span early.
const escCode = (s) => esc(s).replace(/`/g, "'");

const ledger = read("spec/free85/guidebook-command-ledger.yaml");
const keymap = read("spec/free85/keymap.yaml");
const coverage = read("spec/free85/guidebook-coverage.yaml");
const gaps = read("spec/free85/v2-parity-gaps.yaml");

// Appendix A: command catalog grouped by ledger group.
let a = `${HEADER}# Appendix A: Command and Function Catalog\n\n` +
  "Grouped by functional area. Status comes from the Free85 2.0 command " +
  "ledger's status vocabulary: `equivalent` items work today; `partial` and " +
  "`missing` are tracked implementation gaps; `hardware-dependent` items " +
  "await separate hardware validation; `excluded-clean-room` items are " +
  "intentionally out of scope by design, not gaps.\n";
for (const group of ledger.groups) {
  a += `\n## ${esc(group.id)} â€” ${esc(group.kind)} (${group.status})\n\n`;
  if (group.owner) a += `> Tracked under: \`${escCode(group.owner)}\`\n\n`;
  if (group.target) a += `> Target: ${esc(group.target)}\n\n`;
  if (group.reason) a += `> Reason: ${esc(group.reason)}\n\n`;
  a += group.items.map((item) => `\`${escCode(item)}\``).join(", ") + "\n";
}
writeFileSync(`${root}docs/guidebook/appendix-a-command-catalog.md`, a);

// Appendix B: full keymap table.
let b = `${HEADER}# Appendix B: Complete Key Map\n\n` +
  "| Key | Normal | 2nd | ALPHA |\n| --- | --- | --- | --- |\n";
for (const key of keymap.keys) {
  const cell = (m) => (m ? `${esc(m.label)} â€” ${esc(m.behaviour)}` : "â€”");
  b += `| \`${escCode(key.physical_key)}\` | ${cell(key.normal)} | ${cell(key.second)} | ${cell(key.alpha)} |\n`;
}
writeFileSync(`${root}docs/guidebook/appendix-b-keymap.md`, b);

// Appendix D: chapter coverage + open 2.0 gaps.
let d = `${HEADER}# Appendix D: Feature Status and 2.0 Gaps\n\n## Chapter status\n\n` +
  "| Chapter | Topic | Status |\n| --- | --- | --- |\n";
for (const ch of coverage.chapters) {
  d += `| ${ch.chapter} | ${esc(ch.topic)} | ${ch.status}${ch.reason ? ` â€” ${esc(ch.reason)}` : ""} |\n`;
}
d += "\n## Open work packages\n\n" +
  "Every non-`equivalent` entry from the Free85 2.0 parity gap report " +
  "(spec/free85/v2-parity-gaps.yaml), sorted by work package owner. " +
  "`equivalent` areas are already complete and are omitted here (see " +
  "Appendix A for the full catalog).\n\n";
const openGaps = gaps.gaps
  .filter((gap) => gap.status !== "equivalent")
  .map((gap, index) => ({ gap, index }))
  .sort((a, b) =>
    a.gap.owner.localeCompare(b.gap.owner, undefined, { numeric: true }) ||
    a.index - b.index
  )
  .map(({ gap }) => gap);
for (const gap of openGaps) {
  d += `- **${esc(gap.id)}** [${gap.status}, owner \`${escCode(gap.owner)}\`, area ${esc(gap.area)}]: ${esc(gap.target)}\n`;
}
writeFileSync(`${root}docs/guidebook/appendix-d-feature-status.md`, d);
console.log("wrote appendices A, B, D");
